    fun drawMap(canvas: Canvas, row:Int, col:Int, masushurui:Int, sekaix: Int){
        val iro = Paint()
        iro.style = Paint.Style.FILL
        iro.color = argb(255, 30, 30, 30)
        when (masushurui) {
            TYPE_SORA -> { iro.color = argb(255, 155, 155, 250) }
            TYPE_ISHI -> { iro.color = argb(255, 80, 80, 80)}
        }
        //ここまでは色の設定
        //worldOffsetXってのは？
        val xx = MASU_SIZE*col+sekaix
        val yy = MASU_SIZE*row
        canvas.drawRect(shikakuRectXY(xx,yy,MASU_SIZE),iro)
    }

    fun shikakuRectXY(x:Int,y:Int,ookisa:Int): Rect {
        val left = x
        val right = x  + ookisa
        val top = y
        val bottom = y + ookisa
        val m = Rect(left, top, right,bottom)
        return m
    }


    fun masShurui(row:Int,col:Int): Int {
        return masu[row][col]
    }

    //マップの右端を計算する処理
    fun migiMax():Int{
        return (masu[0].size-2) * MASU_SIZE
    }
